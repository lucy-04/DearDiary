// utils/validate.js
// Small middleware that turns express-validator results into a 400 response.
// Put it right after the validation rules in a route.
const { validationResult } = require("express-validator");

const validate = (req, res, next) => {
  const errors = validationResult(req);
  if (!errors.isEmpty()) {
    return res.status(400).json({
      success: false,
      message: "Validation failed",
      errors: errors.array(),
    });
  }
  next();
};

module.exports = validate;
